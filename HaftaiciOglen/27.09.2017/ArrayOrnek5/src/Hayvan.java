
public class Hayvan {
	
	private String ad;
	private String renk;
	private int yas;
	
	public Hayvan(String ad,String renk,int yas){
		this.ad = ad;
		this.renk = renk;
		this.yas = yas;
	}
	
	public String getAd() {
		return ad;
	}
	public void setAd(String ad) {
		this.ad = ad;
	}
	public int getYas() {
		return yas;
	}
	public void setYas(int yas) {
		this.yas = yas;
	}
	public String getRenk() {
		return renk;
	}
	public void setRenk(String renk) {
		this.renk = renk;
	}
	
	

}
