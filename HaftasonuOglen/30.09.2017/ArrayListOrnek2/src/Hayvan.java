
public class Hayvan {
	
	private String renk;
	private int yas;
	
	public Hayvan(){
		
	}
	
	public Hayvan(String renk, int yas) {
		this.renk = renk;
		this.yas = yas;
	}
	
	public String getRenk() {
		return renk;
	}
	public void setRenk(String renk) {
		this.renk = renk;
	}
	public int getYas() {
		return yas;
	}
	public void setYas(int yas) {
		this.yas = yas;
	}
	
	
	

}
